<template>
  <div class="blog">
    <div class="blog-left">
      <router-view></router-view>
    </div>
    <div class="blog-right">
      <div class="tag-class">
        <h3>文章标签</h3>
        <div class="tags" @click="blogByTag($event)">
          <span v-for="tag in tags" class="tag-specifc">{{tag._id}}({{tag.value}})</span>
          <div class="clear"></div>
        </div>
      </div>
      <div class="tag-class">
        <h3>文章归档</h3>
        <div class="tags" @click="blogByTag($event)">
          <span class="time-specifc" v-for="time in times">{{time._id | format_year_month}}({{time.value}})</span>
          <div class="clear"></div>
        </div>
      </div>
      <div class="tag-class">
        <h3>作者简介</h3>
        <div class="author-description">
          苏俊飞，南昌大学，网络工程2014级，大学四年一直在南昌大学家园工作室学习前端开发
        </div>
      </div>
    </div>
    <div class="clear"></div>
    <side></side>
  </div>
</template>

